// EN is the default (HTML content), so we only need LT translations.
// EN values are read from the DOM on first load.

let currentLang = localStorage.getItem('ak-lang') || 'en';
let enDefaults = {};
let heroPhrases = {
    en: ['Websites that move people.', 'Clean code. Sharp design.', 'Built different.', 'AK Studio.'],
    lt: translations.lt['hero.phrases']
};

function collectEnDefaults() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            enDefaults[key] = el.placeholder;
        } else if (el.classList.contains('btn')) {
            const btnText = el.querySelector('.btn-text');
            enDefaults[key] = btnText ? btnText.innerHTML : el.innerHTML;
        } else {
            enDefaults[key] = el.innerHTML;
        }
    });
}

function applyLang(lang) {
    currentLang = lang;
    localStorage.setItem('ak-lang', lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        let value;

        if (lang === 'lt') {
            value = translations.lt[key];
        } else {
            value = enDefaults[key];
        }

        if (value === undefined) return;

        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = value;
        } else if (el.classList.contains('btn') || el.closest('.btn') === el) {
            // Buttons have rain canvas + btn-text wrapper from initMatrixButtons
            const btnText = el.querySelector('.btn-text');
            if (btnText) {
                btnText.innerHTML = value;
            } else {
                el.innerHTML = value;
            }
        } else {
            el.innerHTML = value;
        }
    });

    // Update toggle button
    document.querySelectorAll('.lang-toggle').forEach(btn => {
        btn.textContent = lang === 'en' ? 'LT' : 'EN';
        btn.setAttribute('data-current', lang);
    });

    // Update hero scramble phrases
    if (window.heroScramblePhrases) {
        window.heroScramblePhrases = heroPhrases[lang];
    }

    // Update stagger-hover data-text attributes for nav links
    document.querySelectorAll('.stagger-hover[data-i18n]').forEach(link => {
        const key = link.getAttribute('data-i18n');
        const text = lang === 'lt' ? translations.lt[key] : enDefaults[key];
        if (text) {
            link.setAttribute('data-text', text);
            // Reset any char-span wrappers from stagger hover
            link.innerHTML = text;
        }
    });

    document.documentElement.lang = lang;
}

function initLangToggle() {
    // Collect defaults after a short delay to let app.js initMatrixButtons wrap buttons
    setTimeout(() => {
        collectEnDefaults();

        // Apply saved language
        if (currentLang === 'lt') {
            applyLang('lt');
        }
    }, 50);

    document.querySelectorAll('.lang-toggle').forEach(btn => {
        btn.addEventListener('click', () => {
            const newLang = currentLang === 'en' ? 'lt' : 'en';
            applyLang(newLang);
        });
    });
}

document.addEventListener('DOMContentLoaded', initLangToggle);
